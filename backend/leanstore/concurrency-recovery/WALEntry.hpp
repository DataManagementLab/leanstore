struct WALEntry {
   enum class TYPE : u8 { TX_START, TX_COMMIT, TX_ABORT, DT_SPECIFIC, CARRIAGE_RETURN };
   // -------------------------------------------------------------------------------------
   u16 size;
   TYPE type;
   LID lsn;
   u64 magic_debugging_number = 99;
};
// -------------------------------------------------------------------------------------
struct WALMetaEntry : WALEntry {
   u8 payload[];
};
// -------------------------------------------------------------------------------------
struct WALDTEntry : WALEntry {
   LID gsn;
   DTID dt_id;
   PID pid;
   u8 payload[];
};
